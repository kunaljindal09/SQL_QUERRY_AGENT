import httpx
import json
import asyncio
from typing import Optional, List, Dict, Any
from app.core.config import settings
import re
from google.genai import Client
from google.genai.types import GenerateContentConfig


class LLMService:
    def __init__(self):
        self.provider = settings.LLM_PROVIDER
        self.base_url = settings.LLAMA_BASE_URL
        self.model = settings.LLAMA_MODEL
        self.verify_ssl = settings.LLAMA_VERIFY_SSL
        
        # Initialize Google client only if API key is valid
        self.google_client = None
        if settings.GOOGLE_API_KEY and settings.GOOGLE_API_KEY != "your-google-api-key":
            try:
                self.google_client = Client(api_key=settings.GOOGLE_API_KEY)
            except Exception as e:
                print(f"[LLMService] Failed to initialize Google client: {e}")

    # ------------------------------------------------
    # PUBLIC: GENERATE SQL
    # ------------------------------------------------
    async def generate_sql(
        self,
        question: str,
        schema: Dict[str, Any]
    ) -> Dict[str, Any]:
        """
        Generate SQL with fallback to Google GenAI if primary LLM fails.
        Returns: { sql, explanation } or { sql: "", explanation: "", error: "..." }
        """
        try:
            # Wrap entire operation with 45 second timeout
            return await asyncio.wait_for(
                self._generate_sql_internal(question, schema),
                timeout=45.0
            )
        except asyncio.TimeoutError:
            return {
                "sql": "",
                "explanation": "",
                "error": "LLM request timed out after 45 seconds. Please try again."
            }
    
    async def _generate_sql_internal(
        self,
        question: str,
        schema: Dict[str, Any]
    ) -> Dict[str, Any]:
        """Internal method for SQL generation with fallback logic."""
        if (
            self.provider == "test"
            and "_call_llm" not in self.__dict__
            and "_call_google_llm" not in self.__dict__
        ):
            return {
                "sql": "SELECT 1",
                "explanation": "Deterministic SQL generated by the test LLM provider.",
            }

        schema_text = self._format_schema(schema)
        prompt = self._build_prompt(question, schema_text)

        primary_error = None
        fallback_error = None

        # --- PRIMARY: Ollama/Llama ---
        if self.provider == "llama" and self.base_url:
            try:
                raw = await self._call_llm(prompt)
                parsed = self._parse_json_response(raw)
                result = self._extract_sql_result(parsed)
                if result.get("sql"):
                    return result
            except Exception as e:
                primary_error = str(e)
                print(f"[LLMService] Primary LLM failed: {e}")

        # --- FALLBACK: Google GenAI ---
        if self.google_client:
            try:
                raw = await self._call_google_llm(prompt)
                parsed = self._parse_json_response(raw)
                result = self._extract_sql_result(parsed)
                if result.get("sql"):
                    return result
            except Exception as e:
                fallback_error = str(e)
                print(f"[LLMService] Google fallback failed: {e}")

        # --- BOTH FAILED ---
        error_msg = "LLM service unavailable. "
        if primary_error:
            error_msg += f"Primary: {primary_error[:100]}. "
        if fallback_error:
            error_msg += f"Fallback: {fallback_error[:100]}."
        
        return {"sql": "", "explanation": "", "error": error_msg}

    # ------------------------------------------------
    # PRIMARY: OLLAMA CALL
    # ------------------------------------------------
    async def _call_llm(self, prompt: str) -> str:
        timeout = httpx.Timeout(30.0, connect=5.0)  # Reduced timeout: 30s total, 5s connect
        async with httpx.AsyncClient(verify=self.verify_ssl, timeout=timeout) as client:
            payload = {
                "model": self.model,
                "prompt": prompt,
                "stream": False,
                "options": {
                    "temperature": 0.1,
                    "top_p": 0.9,
                    "num_predict": 2048
                }
            }

            try:
                response = await client.post(
                    f"{self.base_url}/generate",
                    json=payload
                )
            except httpx.ConnectError as e:
                raise Exception(f"Cannot connect to Ollama at {self.base_url}: {str(e)}")
            except httpx.TimeoutException as e:
                raise Exception(f"Ollama request timed out: {str(e)}")
            except httpx.RequestError as e:
                raise Exception(f"Ollama request failed: {str(e)}")

            if response.status_code != 200:
                raise Exception(f"Ollama API error: {response.status_code} - {response.text[:200]}")

            result = response.json()
            raw_text = result.get("response", "")

            if not raw_text:
                raise Exception("Empty response from Ollama")

            return raw_text

    # ------------------------------------------------
    # FALLBACK: GOOGLE GENAI CALL
    # ------------------------------------------------
    async def _call_google_llm(self, prompt: str) -> str:
        if self.google_client is None:
            raise Exception("Google GenAI fallback is not configured")

        response = self.google_client.models.generate_content(
            model="gemini-2.5-flash",
            contents=prompt,
            config=GenerateContentConfig(
                temperature=0,
                top_p=0.9
            )
        )

        raw_text = response.text
        if not raw_text:
            raise Exception("Empty response from Google GenAI")

        return raw_text

    # ------------------------------------------------
    # SHARED: PARSE JSON FROM LLM OUTPUT
    # ------------------------------------------------
    def _parse_json_response(self, raw: str) -> Dict[str, Any]:
        """
        Safely parse JSON from LLM output.
        Handles markdown fences like ```json ... ``` and extracts
        the first JSON object found in the string.
        """
        # Strip markdown code fences if present
        cleaned = re.sub(r"```(?:json)?\s*", "", raw).replace("```", "").strip()

        # Try direct parse first
        try:
            return json.loads(cleaned)
        except json.JSONDecodeError:
            pass

        # Fallback: extract first {...} block from the string
        match = re.search(r"\{.*?\}", cleaned, re.DOTALL)
        if match:
            try:
                return json.loads(match.group())
            except json.JSONDecodeError:
                pass

        raise ValueError(f"Could not parse JSON from LLM response: {cleaned[:200]}")

    def _extract_sql_result(self, parsed: Dict[str, Any]) -> Dict[str, Any]:
        result = parsed.get("data") if isinstance(parsed.get("data"), dict) else parsed
        return {
            "sql": result.get("sql", ""),
            "explanation": result.get("explanation", ""),
        }

    # ------------------------------------------------
    # SHARED: BUILD PROMPT
    # ------------------------------------------------
    def _build_prompt(self, question: str, schema_text: str) -> str:
        return f"""
You are an expert SQL query generator. Based on the database schema and user question below, generate a valid SQL query.

DATABASE SCHEMA:
{schema_text}

USER QUESTION: {question}

RULES:
1. Only generate SELECT queries (no INSERT, UPDATE, DELETE, DROP, or ALTER).
2. Use proper JOINs where needed.
3. The query must be efficient and well-formatted.
4. Explain WHY this query answers the question — mention tables, filters, and joins used.
5. Do not include conversational behaviour or apologies. Just provide the SQL and explanation.
6. If names of the columns are same then give them alias name and the names should be meaningful.
Respond ONLY with a valid JSON object. No markdown, no extra text:
{{
    "sql": "<your SQL query here>",
    "explanation": "<why this query answers the question>"
}}"""

    # ------------------------------------------------
    # SHARED: FORMAT SCHEMA
    # ------------------------------------------------
    def _format_schema(self, schema: Dict[str, Any]) -> str:
        lines = []
        for table in schema.get("tables", []):
            table_name = table.get("table_name")
            columns = table.get("columns", [])
            lines.append(f"\nTable: {table_name}")
            for col in columns:
                nullable = "NULL" if col.get("is_nullable") == "YES" else "NOT NULL"
                lines.append(f"  - {col.get('column_name')}: {col.get('data_type')} ({nullable})")
        return "\n".join(lines)


llm_service = LLMService()
